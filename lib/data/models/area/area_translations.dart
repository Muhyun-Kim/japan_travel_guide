  /// 서비스 지역명 번역
  static String translateServiceArea(String code, String japaneseName) {
    return serviceAreaTranslations[code] ?? japaneseName;
  }

  /// 중지역명 번역
  static String translateMiddleArea(String code, String japaneseName) {
    return middleAreaTranslations[code] ?? japaneseName;
  }

  /// 서비스 지역이 인기 지역인지 확인
  static bool isPopularServiceArea(String code) {
    return popularServiceAreas.contains(code);
  }

  /// 중지역이 인기 지역인지 확인
  static bool isPopularMiddleArea(String code) {
    return popularMiddleAreas.contains(code);
  }
